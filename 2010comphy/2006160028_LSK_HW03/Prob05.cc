// "ls -a" ("a" stands for all)shows every files and directories in that directory. 
//  That is, not ignoring entries starting with.
// "ls -l" ("l" stands for list)shows long information about files, 
// such as permission, owner, groups, size, date and so on. That is, using a long listing format.
// "ls -f" is used when we don't want to sort files in it. (enable -aU, disable -lst)
// "ls -al" shows all and in detail in that directory. ("a" and "l" together.)
// "ls -afl" ignores "f", so execute "a" and "l"
// "ls -alf" ignores "l", so execute "a" and "f".
